function getSynonyms($word) {
    global $dictionary;
    // Normalizar palabra (minúsculas, eliminar acentos si es necesario para la clave, puntuación)
    $clean_word = strtolower(trim($word));
    // Eliminar puntuación común
    $clean_word = preg_replace("/[.,;?!:]/", "", $clean_word);
    
    // Devolver sinónimos si existen
    if (array_key_exists($clean_word, $dictionary)) {
        return $dictionary[$clean_word];
    }
    
    return [];
}

function getRandomSynonym($word) {
    global $dictionary;
    $synonyms = getSynonyms($word);
    if (!empty($synonyms)) {
        return $synonyms[array_rand($synonyms)];
    }
    return $word;
}
